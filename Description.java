public class Description {
    private String text;
    private String condition;

    // Getters and setters
    public String getText() {
        return text;
    }

    public String getCondition() {
        return condition;
    }

    public void setText(String text) {
        this.text = text;
    }

    public void setCondition(String condition) {
        this.condition = condition;
    }
}
